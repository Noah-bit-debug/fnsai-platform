/**
 * Help Center article data.
 *
 * Split out of HelpCenter.tsx so the page component stays small and the
 * content is easy to scan + extend. Each article is an object with an id,
 * category, title, summary, and an array of typed content blocks.
 *
 * Block types:
 *   - p           — paragraph
 *   - h3          — section heading
 *   - steps       — numbered step cards (purple-circled numbers)
 *   - list        — bulleted list
 *   - tip         — green "💡 Tip" callout
 *   - warning     — red "⚠️ Warning" callout
 *   - code        — dark monospace block
 *   - screenshot  — real image (src from /public/help/…) with caption,
 *                   or placeholder dashed box if no src
 *   - mockup      — inline CSS-drawn UI preview (for when a real
 *                   screenshot isn't available yet)
 *   - table       — small comparison table (headers + rows)
 *
 * Categories are free-form strings, but they appear in the sidebar in the
 * order defined by CATEGORY_ORDER at the bottom of this file.
 */

export type Block =
  | { type: 'p'; text: string }
  | { type: 'h3'; text: string }
  | { type: 'steps'; items: string[] }
  | { type: 'list'; items: string[] }
  | { type: 'tip'; text: string }
  | { type: 'warning'; text: string }
  | { type: 'code'; code: string; language?: string }
  | { type: 'screenshot'; src?: string; caption: string }
  | { type: 'mockup'; kind: MockupKind; caption: string }
  | { type: 'table'; headers: string[]; rows: string[][] };

export type MockupKind =
  | 'button-primary'        // ✦ AI Wizard purple button
  | 'button-ghost'          // + Task outlined
  | 'stats-row'             // 4 stat cards in a row
  | 'task-card'             // Recruiter task card with type badge
  | 'toolbar'               // Top toolbar with title + buttons
  | 'sidebar-item'          // Single sidebar nav item
  | 'pipeline-column'       // A single pipeline stage column
  | 'filter-row'            // Row of filter dropdowns
  | 'empty-state'           // Empty state card with CTA
  | 'modal'                 // Generic modal card
  | 'role-badge'            // Colored role badge pill
  | 'integration-pills';    // Connected/disconnected integration pills

export interface Article {
  id: string;
  category: string;
  title: string;
  summary: string;
  blocks: Block[];
}

